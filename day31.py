#forstatement
print("For pada list")
l = ["aku", "mau", "makan"]
for i in l:
	print(i)
print("\nfor pada tuple")
t = ("aku", "mau", "makam")
for i in t:
	print(i)
print("\nfor pada string")
s = "aku mau makan"
for i in s:
	print(i)
print("\nfor pada dictiooary")
d = dict()
d['a'] = 'aku mau makan'
d['b'] = 'aku mau minum'
for i in d:
	print(i, d[i])
